"""
Loads the 700-call benchmark generated by the MTP-1 dispatcher agent
(``outputs.json``).  Each entry contains:

* ``clean_conversation`` - Operator/Caller dialogue (ground truth speech)
* ``transcript``         - Whisper output (noisy reference)
* ``extracted_info``     - Ground truth structured record
* ``asr_metrics``        - Original ASR quality stats (WER, latency, conf.)
* ``simulation``         - Downstream dispatch timing

The object returned by :func:`load_dataset` is a list of :class:`Call`
records so the rest of the codebase never has to know about JSON keys.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional


@dataclass
class Call:
    call_id: int
    clean_conversation: str
    transcript: str
    extracted_info: Dict[str, Any]
    asr_metrics: Dict[str, float] = field(default_factory=dict)
    simulation: Dict[str, Any] = field(default_factory=dict)

    @property
    def is_noisy(self) -> bool:
        """A call is considered noisy when its original WER >= 0.15."""
        return float(self.asr_metrics.get("wer", 0)) >= 0.15


def load_dataset(path: Optional[str] = None,
                 limit: Optional[int] = None) -> List[Call]:
    """Parse outputs.json into :class:`Call` records."""
    default = Path(__file__).resolve().parent.parent.parent / "uploads" / "outputs.json"
    p = Path(path) if path else default
    if not p.exists():
        # fallback to common mount locations
        for candidate in ["/sessions/eager-brave-bell/mnt/uploads/outputs.json",
                          str(Path.cwd() / "uploads" / "outputs.json")]:
            if Path(candidate).exists():
                p = Path(candidate)
                break
    with open(p, "r", encoding="utf-8") as fh:
        raw = json.load(fh)
    records = []
    for r in raw:
        records.append(Call(
            call_id=r["call_id"],
            clean_conversation=r["clean_conversation"],
            transcript=r["transcript"],
            extracted_info=r["extracted_info"],
            asr_metrics=r.get("asr_metrics", {}),
            simulation=r.get("simulation", {}),
        ))
        if limit and len(records) >= limit:
            break
    return records
